document.addEventListener("DOMContentLoaded", () => {
    // URL'den dil parametresi kontrolü (?lang=en gibi)
    const urlParams = new URLSearchParams(window.location.search);
    let lang = urlParams.get('lang');

    // Eğer URL'de yoksa tarayıcı dilini al
    if (!lang) {
        lang = navigator.language.slice(0, 2);
    }

    // Dil listede yoksa varsayılan olarak İngilizce yap
    if (!translations[lang]) {
        lang = "en";
    }

    // HTML lang etiketini güncelle (SEO için kritik)
    document.documentElement.lang = lang;

    // Sayfadaki tüm data-i18n etiketlerini bul ve çevir
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });
});
